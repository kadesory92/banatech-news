<?php

namespace App\Http\Controllers\Admin;

use App\Http\Controllers\Controller;
use App\Http\Requests\PostRequest;
use App\Models\Category;
use App\Models\Post;
use App\Models\Tag;
use Illuminate\Http\Request;

class PostsController extends Controller
{
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $posts=Post::paginate(3);
        return view('admin.posts.index', compact('posts'));
        //
    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $categories=Category::all();
        return view('admin.posts.create', compact('categories'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(PostRequest $request)
    {
        $data=$request->validated();

        $post = new Post();
        $post->category_id = $data['category_id'];
        $post->title = $data['title'];
        $post->slug = $data['slug'];
        $post->content = $data['content'];
        $image = $data['image'];
        $image = $request->file("image");
        $filename = md5(uniqid()) . "." . $image->getClientOriginalExtension();
        $image->move(public_path("uploads/posts"), $filename);
        $post->image = $filename;
        $post->is_featured = $request->is_featured==true?'1':'0';
        $post->is_published = $request->is_published==true?'1':'0';
        $post->save();
        return redirect(route('post.index'))->with("message", "Post created successfully");
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show(Post $post)
    {
        return view('admin.posts.show', compact('post'));
    }

    /**
     * Show the form for editing the specified resource.
